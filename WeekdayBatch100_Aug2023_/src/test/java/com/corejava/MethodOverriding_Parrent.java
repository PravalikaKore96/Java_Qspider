
package com.corejava;

public class MethodOverriding_Parrent {
	
	
	
	public void birth(String birth) {
		System.out.println("Parrent : "+birth);
	}
	
	public void education(String education) {
		System.out.println("Parrent : "+education);
	}
	
	
	public void engagement(String engagement,int date) {
		System.out.println("Parrent : "+engagement+date);
	}
	
	
	public void marriage(String marriage,int date) {
		System.out.println("Parrent : "+marriage+date);
	}
	
	public void assets() {
		System.out.println("Parrent : Assets");
	}
	
	

}